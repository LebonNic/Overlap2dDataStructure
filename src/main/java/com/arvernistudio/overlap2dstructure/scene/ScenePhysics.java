package com.arvernistudio.overlap2dstructure.scene;

/**
 * @author nprugne
 */
public class ScenePhysics {

    /**
     * Default constructor
     */
    public ScenePhysics() {
    }

    /**
     * Tells runtime if it should prepare a physics engine
     */
    public boolean enabled;

    /**
     * X component of gravity vector if any
     */
    public float gravityX;

    /**
     * Y component of gravity vector if any
     */
    public float gravityY;


}